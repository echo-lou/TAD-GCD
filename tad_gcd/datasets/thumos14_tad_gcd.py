def pad_feature_list(
    features: List[Optional[torch.Tensor]],
) -> Tuple[Optional[torch.Tensor], Optional[torch.Tensor], List[int]]:
    """
    Pad a list of T x D features.

    Returns:
        padded_features:
            B x Tmax x D, or None if all features are None.

        feature_mask:
            B x Tmax bool tensor.
            True means valid timestep.

        lengths:
            List[int].
    """
    if all(f is None for f in features):
        return None, None, [0 for _ in features]

    if any(f is None for f in features):
        raise ValueError(
            "Some features are None while others are not. "
            "Please check feature paths or use metadata-only mode."
        )

    assert all(f is not None for f in features)
    lengths = [int(f.shape[0]) for f in features]  # type: ignore
    dims = [int(f.shape[1]) for f in features]     # type: ignore

    if len(set(dims)) != 1:
        raise ValueError(f"Feature dims are inconsistent in batch: {dims}")

    batch_size = len(features)
    max_len = max(lengths)
    feat_dim = dims[0]

    padded = torch.zeros((batch_size, max_len, feat_dim), dtype=torch.float32)
    mask = torch.zeros((batch_size, max_len), dtype=torch.bool)

    for i, feat in enumerate(features):
        assert feat is not None
        t = feat.shape[0]
        padded[i, :t] = feat
        mask[i, :t] = True

    return padded, mask, lengths


def tad_gcd_collate_fn(batch: List[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Collate function for TAD-GCD.

    Output:
        {
            "video_id": List[str],
            "video_path": List[Optional[str]],
            "feat_path": List[Optional[str]],
            "mode": List[str],

            "features": Tensor[B, Tmax, D] or None,
            "feature_mask": BoolTensor[B, Tmax] or None,
            "feature_lengths": List[int],

            "segments": List[Tensor[Ni, 2]],
            "labels": List[Tensor[Ni]],
            "class_types": List[List[str]],
            "label_names": List[List[str]],

            "annotations": List[List[Dict]]
        }
    """
    features = [x.get("feature", None) for x in batch]
    padded_features, feature_mask, feature_lengths = pad_feature_list(features)

    out = {
        "video_id": [x["video_id"] for x in batch],
        "video_path": [x.get("video_path", None) for x in batch],
        "feat_path": [x.get("feat_path", None) for x in batch],
        "mode": [x["mode"] for x in batch],
        "features": padded_features,
        "feature_mask": feature_mask,
        "feature_lengths": feature_lengths,
        "segments": [x["segments"] for x in batch],
        "labels": [x["labels"] for x in batch],
        "class_types": [x["class_types"] for x in batch],
        "label_names": [x["label_names"] for x in batch],
        "num_annotations": [x["num_annotations"] for x in batch],
    }

    if "annotations" in batch[0]:
        out["annotations"] = [x.get("annotations", []) for x in batch]

    for optional_key in ["duration", "fps", "feature_fps", "num_frames", "subset"]:
        if optional_key in batch[0]:
            out[optional_key] = [x.get(optional_key, None) for x in batch]

    return out


