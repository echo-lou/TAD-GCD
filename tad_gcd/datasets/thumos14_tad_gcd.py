def run_all_mode_check(
    index_json: str,
    batch_size: int = 2,
    num_workers: int = 0,
    strict: bool = True,
    feature_dir: Optional[str] = None,
    feature_ext: str = ".npy",
    load_features: bool = False,
    feature_norm: str = "none",
    allow_missing_feature: bool = True,
) -> None:
    modes = ["train_labeled", "train_unlabeled", "test"]

    # Optional modes may not exist in old split json.
    with open(index_json, "r", encoding="utf-8") as f:
        raw = json.load(f)
    for m in ["test_old", "test_new"]:
        if m in raw and isinstance(raw[m], list) and len(raw[m]) > 0:
            modes.append(m)

    for mode in modes:
        print("\n\n")
        print("#" * 80)
        print(f"Checking mode: {mode}")
        print("#" * 80)

        dataset, loader = build_tad_gcd_dataloader(
            index_json=index_json,
            mode=mode,
            batch_size=batch_size,
            num_workers=num_workers,
            strict=strict,
            feature_dir=feature_dir,
            feature_ext=feature_ext,
            load_features=load_features,
            feature_norm=feature_norm,
            allow_missing_feature=allow_missing_feature,
        )

        dataset.print_summary(show_classes=(mode == "train_labeled"), show_counts=True)
        inspect_first_samples(dataset, n=2)
        inspect_first_batch(loader)

        s = dataset.summary()

        if mode == "train_labeled":
            assert s["num_old_annotations"] > 0, "train_labeled should contain old annotations."
            assert s["num_new_annotations"] == 0, "train_labeled must not contain new annotations."

        if mode == "train_unlabeled":
            assert s["num_annotations"] == 0, "train_unlabeled must have zero annotations."

        if mode == "test":
            assert s["num_annotations"] > 0, "test should contain annotations."
            if s["num_new_annotations"] == 0:
                print("WARNING: test contains zero new annotations. Please check split json.")

        if mode == "test_old":
            assert s["num_new_annotations"] == 0, "test_old must not contain new annotations."

        if mode == "test_new":
            assert s["num_old_annotations"] == 0, "test_new must not contain old annotations."


