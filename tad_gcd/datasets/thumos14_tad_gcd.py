def build_tad_gcd_dataloader(
    index_json: str,
    mode: str,
    batch_size: int = 4,
    shuffle: Optional[bool] = None,
    num_workers: int = 0,
    feature_dir: Optional[str] = None,
    feature_ext: str = ".npy",
    load_features: bool = False,
    feature_norm: str = "none",
    strict: bool = True,
    allow_missing_video_path: bool = True,
    allow_missing_feature: bool = True,
    return_video_path: bool = True,
    return_raw_annotations: bool = True,
    pin_memory: bool = False,
    drop_last: bool = False,
) -> Tuple[THUMOS14TADGCDDataset, DataLoader]:
    if shuffle is None:
        shuffle = mode in {"train_labeled", "train_unlabeled"}

    dataset = build_tad_gcd_dataset(
        index_json=index_json,
        mode=mode,
        feature_dir=feature_dir,
        feature_ext=feature_ext,
        load_features=load_features,
        feature_norm=feature_norm,
        strict=strict,
        allow_missing_video_path=allow_missing_video_path,
        allow_missing_feature=allow_missing_feature,
        return_video_path=return_video_path,
        return_raw_annotations=return_raw_annotations,
    )

    loader = DataLoader(
        dataset,
        batch_size=batch_size,
        shuffle=shuffle,
        num_workers=num_workers,
        collate_fn=tad_gcd_collate_fn,
        pin_memory=pin_memory,
        drop_last=drop_last,
    )

    return dataset, loader


def build_dataset(cfg: Union[Dict[str, Any], Any], mode: str) -> THUMOS14TADGCDDataset:
    """
    Generic builder for future training code.

    Supports dict config:

        cfg = {
            "index_json": "...",
            "feature_dir": "...",
            "feature_ext": ".npy",
            "load_features": True,
            "feature_norm": "l2",
        }

    Or object-style config with attributes.
    """
    def get_cfg(name: str, default: Any = None) -> Any:
        if isinstance(cfg, dict):
            return cfg.get(name, default)
        return getattr(cfg, name, default)

    return build_tad_gcd_dataset(
        index_json=get_cfg("index_json"),
        mode=mode,
        feature_dir=get_cfg("feature_dir", None),
        feature_ext=get_cfg("feature_ext", ".npy"),
        load_features=get_cfg("load_features", False),
        feature_norm=get_cfg("feature_norm", "none"),
        strict=get_cfg("strict", True),
        allow_missing_video_path=get_cfg("allow_missing_video_path", True),
        allow_missing_feature=get_cfg("allow_missing_feature", True),
        return_video_path=get_cfg("return_video_path", True),
        return_raw_annotations=get_cfg("return_raw_annotations", True),
    )


