#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
THUMOS14-TAD-GCD Dataset Loader.

This file reads the split json generated by:

    datasets/build_thumos14_tad_gcd_index.py
    datasets/build_thumos14_tad_gcd_index_code2114.py

Expected split json examples:

    data/thumos14_tad_gcd_split_seed0.json
    data/thumos14_tad_gcd_code2114_train50_split0.json
    data/thumos14_tad_gcd_code2114_train50_split0_ratio100.json

Supported modes:

    train_labeled
    train_unlabeled
    test
    test_old
    test_new

Main protocol:

    1. train_labeled:
        - returns only old-class annotations.
        - new-class annotations must never appear.
        - used for supervised temporal action detection training.

    2. train_unlabeled:
        - returns video_id / video_path / feat_path.
        - annotations must be [].
        - used for unsupervised representation learning, proposal mining,
          and unknown candidate discovery.

    3. test:
        - returns old + new annotations.
        - old classes have label_id.
        - new classes have eval_id.
        - used only for evaluation.

    4. test_old:
        - returns old annotations only.
        - convenient for old-class mAP.

    5. test_new:
        - returns new annotations only.
        - convenient for unknown discovery / clustering evaluation.

Feature support:

    This dataset can optionally load pre-extracted features from:

        .npy
        .pt / .pth
        .pkl / .pickle

    The unified output feature shape is:

        T x D

    If feature loading is disabled, the dataset works as a metadata-level loader.

Important leakage rule:

    Training code must not use:
        - new-class labels;
        - new-class temporal boundaries;
        - new-class names;
        - new-class text prompts;
        - new_label_to_eval_id.

Usage:

    Metadata-only check:

        python datasets/thumos14_tad_gcd.py \
            --index_json data/thumos14_tad_gcd_code2114_train50_split0.json \
            --mode train_labeled

    Feature-level check:

        python datasets/thumos14_tad_gcd.py \
            --index_json data/thumos14_tad_gcd_code2114_train50_split0.json \
            --mode train_labeled \
            --load_features \
            --feature_dir /path/to/features \
            --feature_ext .npy

    Check all modes:

        python datasets/thumos14_tad_gcd.py \
            --index_json data/thumos14_tad_gcd_code2114_train50_split0.json \
            --check_all_modes
"""

import argparse
import json
import pickle
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple, Union

import numpy as np
import torch
import torch.nn.functional as F
from torch.utils.data import Dataset, DataLoader


VALID_MODES = {
    "train_labeled",
    "train_unlabeled",
    "test",
    "test_old",
    "test_new",
}

FEATURE_EXTS = {".npy", ".pt", ".pth", ".pkl", ".pickle"}


def _as_float(x: Any, default: float = 0.0) -> float:
    try:
        return float(x)
    except Exception:
        return float(default)


def _as_int(x: Any, default: int = -1) -> int:
    try:
        return int(x)
    except Exception:
        return int(default)


def _safe_path_exists(path: Optional[str]) -> bool:
    if path is None:
        return False
    try:
        return Path(path).exists()
    except Exception:
        return False


def _to_tensor_feature(obj: Any) -> torch.Tensor:
    """
    Convert loaded feature object to torch.FloatTensor with shape T x D.

    Supported loaded object examples:
        np.ndarray
        torch.Tensor
        {"features": array}
        {"feat": array}
        {"feature": array}
        {"video_feature": array}
    """
    if isinstance(obj, dict):
        candidate_keys = [
            "features",
            "feature",
            "feat",
            "video_feature",
            "video_features",
            "data",
            "arr_0",
        ]
        value = None
        for k in candidate_keys:
            if k in obj:
                value = obj[k]
                break
        if value is None:
            # fallback: first array-like value
            for v in obj.values():
                if isinstance(v, (np.ndarray, torch.Tensor, list, tuple)):
                    value = v
                    break
        if value is None:
            raise ValueError(f"Cannot find feature array in dict keys: {list(obj.keys())}")
        obj = value

    if isinstance(obj, np.lib.npyio.NpzFile):
        keys = list(obj.keys())
        if not keys:
            raise ValueError("Empty npz feature file.")
        # Prefer common keys, otherwise first key.
        key = None
        for k in ["features", "feature", "feat", "arr_0"]:
            if k in obj:
                key = k
                break
        if key is None:
            key = keys[0]
        obj = obj[key]

    if isinstance(obj, torch.Tensor):
        feat = obj.detach().cpu().float()
    elif isinstance(obj, np.ndarray):
        feat = torch.from_numpy(obj).float()
    elif isinstance(obj, (list, tuple)):
        feat = torch.tensor(obj, dtype=torch.float32)
    else:
        raise TypeError(f"Unsupported feature object type: {type(obj)}")

    if feat.ndim == 1:
        feat = feat.unsqueeze(0)

    if feat.ndim != 2:
        raise ValueError(f"Expected feature shape T x D, got {tuple(feat.shape)}")

    return feat.contiguous()


def load_feature_file(path: str) -> torch.Tensor:
    """
    Load feature file and return FloatTensor of shape T x D.

    Supported:
        .npy
        .npz
        .pt / .pth
        .pkl / .pickle
    """
    p = Path(path)
    if not p.exists():
        raise FileNotFoundError(f"Feature file not found: {p}")

    ext = p.suffix.lower()

    if ext == ".npy":
        obj = np.load(str(p), allow_pickle=True)
        return _to_tensor_feature(obj)

    if ext == ".npz":
        obj = np.load(str(p), allow_pickle=True)
        return _to_tensor_feature(obj)

    if ext in {".pt", ".pth"}:
        obj = torch.load(str(p), map_location="cpu")
        return _to_tensor_feature(obj)

    if ext in {".pkl", ".pickle"}:
        with open(p, "rb") as f:
            obj = pickle.load(f)
        return _to_tensor_feature(obj)

    raise ValueError(f"Unsupported feature extension: {ext}. Supported: {sorted(FEATURE_EXTS)}")


def normalize_feature(feat: torch.Tensor, mode: str = "none", eps: float = 1e-6) -> torch.Tensor:
    """
    Normalize feature.

    mode:
        none:
            no normalization.

        l2:
            L2 normalize along feature dimension D.

        zscore:
            normalize each feature dimension over temporal dimension.
            This is optional and mainly for debugging.
    """
    mode = str(mode).lower()

    if mode in {"none", "no", "false", ""}:
        return feat

    if mode == "l2":
        return F.normalize(feat, p=2, dim=-1, eps=eps)

    if mode == "zscore":
        mean = feat.mean(dim=0, keepdim=True)
        std = feat.std(dim=0, keepdim=True).clamp_min(eps)
        return (feat - mean) / std

    raise ValueError(f"Unknown feature_norm mode: {mode}")


class THUMOS14TADGCDDataset(Dataset):
    """
    THUMOS14-TAD-GCD dataset.

    This class supports both:
        1. metadata-level loading;
        2. feature-level loading.

    Parameters
    ----------
    index_json:
        Path to generated TAD-GCD split json.

    mode:
        One of:
            train_labeled
            train_unlabeled
            test
            test_old
            test_new

    feature_dir:
        Optional feature directory. If feat_path is missing in split json,
        the dataset will try to locate features from feature_dir by video_id.

    feature_ext:
        Feature file extension, such as .npy, .pt, .pkl.

    load_features:
        Whether to load features in __getitem__.

    feature_norm:
        none / l2 / zscore.

    return_video_path:
        Whether to include video_path in output.

    return_raw_annotations:
        Whether to include original annotation dicts in output.

    strict:
        Whether to run strict integrity and leakage checks.

    allow_missing_video_path:
        If False, raises error when video_path is missing.

    allow_missing_feature:
        If False, raises error when feature file is missing.
        If True, feature can be None when load_features=False.
    """

    def __init__(
        self,
        index_json: str,
        mode: str = "train_labeled",
        feature_dir: Optional[str] = None,
        feature_ext: str = ".npy",
        load_features: bool = False,
        feature_norm: str = "none",
        return_video_path: bool = True,
        return_raw_annotations: bool = True,
        strict: bool = True,
        allow_missing_video_path: bool = True,
        allow_missing_feature: bool = True,
        copy_annotations: bool = True,
    ):
        super().__init__()

        if mode not in VALID_MODES:
            raise ValueError(f"Invalid mode: {mode}. Expected one of: {sorted(VALID_MODES)}")

        self.index_json = str(index_json)
        self.mode = mode
        self.feature_dir = str(feature_dir) if feature_dir is not None else None
        self.feature_ext = feature_ext if feature_ext.startswith(".") else "." + feature_ext
        self.load_features = bool(load_features)
        self.feature_norm = str(feature_norm).lower()
        self.return_video_path = bool(return_video_path)
        self.return_raw_annotations = bool(return_raw_annotations)
        self.strict = bool(strict)
        self.allow_missing_video_path = bool(allow_missing_video_path)
        self.allow_missing_feature = bool(allow_missing_feature)
        self.copy_annotations = bool(copy_annotations)

        index_path = Path(self.index_json)
        if not index_path.exists():
            raise FileNotFoundError(f"Index json not found: {index_path}")

        with open(index_path, "r", encoding="utf-8") as f:
            self.data = json.load(f)

        self.dataset_name = self.data.get("dataset", "THUMOS14")
        self.task_name = self.data.get("task", "TAD-GCD")
        self.seed = self.data.get("seed", None)
        self.protocol_version = self.data.get("protocol_version", None)

        self.class_split = self.data.get("class_split", {})
        self.old_classes = self.class_split.get("old_classes", [])
        self.new_classes = self.class_split.get("new_classes", [])

        self.label_mapping = self.data.get("label_mapping", {})
        self.old_label_to_id = self.label_mapping.get("old_label_to_id", {})
        self.new_label_to_eval_id = self.label_mapping.get("new_label_to_eval_id", {})

        self.old_id_to_label = {int(v): k for k, v in self.old_label_to_id.items()}
        self.new_eval_id_to_label = {int(v): k for k, v in self.new_label_to_eval_id.items()}

        self.samples = self.data.get(mode, [])

        if self.strict:
            self.basic_check()
            self.check_no_leakage()
            self.check_video_path()
            self.check_feature_path()

    # -------------------------------------------------------------------------
    # Checks
    # -------------------------------------------------------------------------

    def basic_check(self) -> None:
        if not isinstance(self.samples, list):
            raise TypeError(f"Expected samples to be list, got {type(self.samples)} for mode={self.mode}")

        if len(self.samples) == 0:
            raise RuntimeError(
                f"Dataset mode '{self.mode}' is empty. Please check index json: {self.index_json}"
            )

        if not isinstance(self.old_classes, list) or len(self.old_classes) == 0:
            raise RuntimeError("old_classes is empty or invalid in index json.")

        if not isinstance(self.new_classes, list) or len(self.new_classes) == 0:
            raise RuntimeError("new_classes is empty or invalid in index json.")

        if not isinstance(self.old_label_to_id, dict) or len(self.old_label_to_id) == 0:
            raise RuntimeError("old_label_to_id is empty or invalid in index json.")

        if not isinstance(self.new_label_to_eval_id, dict) or len(self.new_label_to_eval_id) == 0:
            raise RuntimeError("new_label_to_eval_id is empty or invalid in index json.")

        for cls in self.old_classes:
            if cls not in self.old_label_to_id:
                raise RuntimeError(f"Old class missing from old_label_to_id: {cls}")

        for cls in self.new_classes:
            if cls not in self.new_label_to_eval_id:
                raise RuntimeError(f"New class missing from new_label_to_eval_id: {cls}")

        overlap = sorted(set(self.old_classes).intersection(set(self.new_classes)))
        if overlap:
            raise RuntimeError(f"old_classes and new_classes overlap: {overlap}")

    def check_no_leakage(self) -> None:
        """
        Information leakage check.

        Rules:
            train_labeled:
                only old annotations are allowed.

            train_unlabeled:
                annotations must be [].

            test:
                old and new annotations are allowed.

            test_old:
                only old annotations are allowed.

            test_new:
                only new annotations are allowed.
        """
        new_set = set(self.new_classes)
        old_set = set(self.old_classes)

        if self.mode == "train_labeled":
            leaked = []
            invalid_old = []

            for sample in self.samples:
                video_id = sample.get("video_id", None)

                for ann in sample.get("annotations", []):
                    label = ann.get("label", None)
                    class_type = ann.get("class_type", None)

                    if label in new_set or class_type == "new" or "eval_id" in ann:
                        leaked.append({"video_id": video_id, "annotation": ann})

                    if label not in old_set or class_type != "old" or "label_id" not in ann:
                        invalid_old.append({"video_id": video_id, "annotation": ann})

            if leaked:
                raise RuntimeError(
                    "New-class annotation leakage detected in train_labeled. "
                    f"First leaked item: {leaked[0]}"
                )

            if invalid_old:
                raise RuntimeError(
                    "Invalid annotation detected in train_labeled. "
                    f"First invalid item: {invalid_old[0]}"
                )

        elif self.mode == "train_unlabeled":
            bad = []

            for sample in self.samples:
                anns = sample.get("annotations", None)
                if anns != []:
                    bad.append({"video_id": sample.get("video_id", None), "annotations": anns})

            if bad:
                raise RuntimeError(
                    "train_unlabeled must have empty annotations. "
                    f"First bad sample: {bad[0]}"
                )

        elif self.mode in {"test", "test_old", "test_new"}:
            invalid = []

            for sample in self.samples:
                video_id = sample.get("video_id", None)

                for ann in sample.get("annotations", []):
                    label = ann.get("label", None)
                    class_type = ann.get("class_type", None)

                    if self.mode == "test_old" and class_type != "old":
                        invalid.append({
                            "video_id": video_id,
                            "annotation": ann,
                            "reason": "test_old can only contain old annotations",
                        })

                    if self.mode == "test_new" and class_type != "new":
                        invalid.append({
                            "video_id": video_id,
                            "annotation": ann,
                            "reason": "test_new can only contain new annotations",
                        })

                    if class_type == "old":
                        if label not in old_set or "label_id" not in ann:
                            invalid.append({
                                "video_id": video_id,
                                "annotation": ann,
                                "reason": "invalid old annotation",
                            })

                    elif class_type == "new":
                        if label not in new_set or "eval_id" not in ann:
                            invalid.append({
                                "video_id": video_id,
                                "annotation": ann,
                                "reason": "invalid new annotation",
                            })

                    else:
                        invalid.append({
                            "video_id": video_id,
                            "annotation": ann,
                            "reason": "unknown class_type",
                        })

            if invalid:
                raise RuntimeError(
                    f"Invalid annotation detected in {self.mode}. "
                    f"First invalid item: {invalid[0]}"
                )

    def check_video_path(self) -> None:
        if self.allow_missing_video_path:
            return

        missing = []
        for sample in self.samples:
            path = sample.get("video_path", None)
            if path is None or not Path(path).exists():
                missing.append({"video_id": sample.get("video_id", None), "video_path": path})

        if missing:
            raise FileNotFoundError(
                f"Missing video_path detected. First missing sample: {missing[0]}"
            )

    def check_feature_path(self) -> None:
        if self.allow_missing_feature:
            return

        missing = []
        for sample in self.samples:
            path = self.resolve_feature_path(sample)
            if path is None or not Path(path).exists():
                missing.append({"video_id": sample.get("video_id", None), "feat_path": path})

        if missing:
            raise FileNotFoundError(
                f"Missing feature detected. First missing sample: {missing[0]}"
            )

    # -------------------------------------------------------------------------
    # Feature path / loading
    # -------------------------------------------------------------------------

    def resolve_feature_path(self, sample: Dict[str, Any]) -> Optional[str]:
        """
        Resolve feature path for a sample.

        Priority:
            1. sample["feat_path"]
            2. feature_dir / (video_id + feature_ext)
            3. feature_dir / (video_id + ".npy/.pt/.pth/.pkl/.pickle")
        """
        if "feat_path" in sample and sample["feat_path"] is not None:
            return sample["feat_path"]

        if self.feature_dir is None:
            return None

        video_id = sample.get("video_id", "")
        root = Path(self.feature_dir)

        # Try configured extension first.
        p = root / f"{video_id}{self.feature_ext}"
        if p.exists():
            return str(p)

        # Try common extensions.
        for ext in [".npy", ".npz", ".pt", ".pth", ".pkl", ".pickle"]:
            p = root / f"{video_id}{ext}"
            if p.exists():
                return str(p)

        # Try recursive search as a slow fallback.
        for ext in [self.feature_ext, ".npy", ".npz", ".pt", ".pth", ".pkl", ".pickle"]:
            matches = list(root.rglob(f"{video_id}{ext}"))
            if matches:
                return str(matches[0])

        return str(root / f"{video_id}{self.feature_ext}")

    def load_feature(self, sample: Dict[str, Any]) -> Tuple[Optional[torch.Tensor], Optional[str]]:
        feat_path = self.resolve_feature_path(sample)

        if not self.load_features:
            return None, feat_path

        if feat_path is None or not Path(feat_path).exists():
            if self.allow_missing_feature:
                return None, feat_path
            raise FileNotFoundError(f"Feature file not found for video {sample.get('video_id')}: {feat_path}")

        feat = load_feature_file(feat_path)
        feat = normalize_feature(feat, mode=self.feature_norm)

        return feat, feat_path

    # -------------------------------------------------------------------------
    # Annotation conversion
    # -------------------------------------------------------------------------

    def _copy_ann_list(self, annotations: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
        if not self.copy_annotations:
            return annotations
        return [dict(x) for x in annotations]

    def annotations_to_tensors(
        self,
        annotations: List[Dict[str, Any]],
    ) -> Tuple[torch.Tensor, torch.Tensor, List[str], List[str]]:
        """
        Convert annotation dicts to tensors.

        Returns:
            segments:
                FloatTensor, shape N x 2, in seconds.

            labels:
                LongTensor, shape N.
                old class uses label_id.
                new class uses eval_id.
                train_unlabeled returns empty tensor.

            class_types:
                List[str], old/new.

            label_names:
                List[str].
        """
        segments: List[List[float]] = []
        labels: List[int] = []
        class_types: List[str] = []
        label_names: List[str] = []

        for ann in annotations:
            ctype = ann.get("class_type", "")
            label_name = ann.get("label", "")

            start = _as_float(ann.get("start", ann.get("t_start", 0.0)))
            end = _as_float(ann.get("end", ann.get("t_end", 0.0)))

            if end <= start:
                continue

            if ctype == "old":
                label = _as_int(ann.get("label_id", -1))
            elif ctype == "new":
                label = _as_int(ann.get("eval_id", -1))
            else:
                label = -1

            if label < 0:
                continue

            segments.append([start, end])
            labels.append(label)
            class_types.append(ctype)
            label_names.append(label_name)

        if segments:
            segments_tensor = torch.tensor(segments, dtype=torch.float32)
            labels_tensor = torch.tensor(labels, dtype=torch.long)
        else:
            segments_tensor = torch.zeros((0, 2), dtype=torch.float32)
            labels_tensor = torch.zeros((0,), dtype=torch.long)

        return segments_tensor, labels_tensor, class_types, label_names

    # -------------------------------------------------------------------------
    # Dataset API
    # -------------------------------------------------------------------------

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> Dict[str, Any]:
        raw = self.samples[idx]

        video_id = raw.get("video_id", "")
        annotations = raw.get("annotations", [])

        feat, feat_path = self.load_feature(raw)
        segments, labels, class_types, label_names = self.annotations_to_tensors(annotations)

        item: Dict[str, Any] = {
            "video_id": video_id,
            "mode": self.mode,
            "feature": feat,
            "feat_path": feat_path,
            "segments": segments,
            "labels": labels,
            "class_types": class_types,
            "label_names": label_names,
            "num_annotations": int(segments.shape[0]),
        }

        if self.return_raw_annotations:
            item["annotations"] = self._copy_ann_list(annotations)

        if self.return_video_path:
            item["video_path"] = raw.get("video_path", None)

        # Optional metadata if present in future split json.
        for key in ["duration", "fps", "feature_fps", "num_frames", "subset"]:
            if key in raw:
                item[key] = raw[key]

        return item

    # -------------------------------------------------------------------------
    # Accessors / summaries
    # -------------------------------------------------------------------------

    def get_class_split(self) -> Dict[str, List[str]]:
        return {
            "old_classes": list(self.old_classes),
            "new_classes": list(self.new_classes),
        }

    def get_label_mapping(self) -> Dict[str, Dict[str, int]]:
        return {
            "old_label_to_id": dict(self.old_label_to_id),
            "new_label_to_eval_id": dict(self.new_label_to_eval_id),
        }

    def get_old_classes(self) -> List[str]:
        return list(self.old_classes)

    def get_new_classes(self) -> List[str]:
        return list(self.new_classes)

    def get_old_label_to_id(self) -> Dict[str, int]:
        return dict(self.old_label_to_id)

    def get_new_label_to_eval_id(self) -> Dict[str, int]:
        return dict(self.new_label_to_eval_id)

    def get_old_id_to_label(self) -> Dict[int, str]:
        return dict(self.old_id_to_label)

    def get_new_eval_id_to_label(self) -> Dict[int, str]:
        return dict(self.new_eval_id_to_label)

    def get_statistics(self) -> Dict[str, Any]:
        return self.data.get("statistics", {})

    def get_raw_index_data(self) -> Dict[str, Any]:
        """
        Return raw json object.

        Warning:
            Training code should not read new-class information from raw data.
        """
        return self.data

    def count_annotations(self) -> Dict[str, int]:
        out = {
            "total": 0,
            "old": 0,
            "new": 0,
            "unknown_type": 0,
        }

        for sample in self.samples:
            for ann in sample.get("annotations", []):
                out["total"] += 1
                ctype = ann.get("class_type", None)
                if ctype == "old":
                    out["old"] += 1
                elif ctype == "new":
                    out["new"] += 1
                else:
                    out["unknown_type"] += 1

        return out

    def count_by_class(self) -> Dict[str, int]:
        counts: Dict[str, int] = {}

        for sample in self.samples:
            for ann in sample.get("annotations", []):
                label = ann.get("label", "UNKNOWN")
                counts[label] = counts.get(label, 0) + 1

        return dict(sorted(counts.items(), key=lambda x: x[0]))

    def count_paths(self) -> Dict[str, int]:
        video_path_count = sum(1 for x in self.samples if x.get("video_path", None) is not None)
        feat_path_count = 0
        existing_feat_count = 0

        for x in self.samples:
            feat_path = self.resolve_feature_path(x)
            if feat_path is not None:
                feat_path_count += 1
                if Path(feat_path).exists():
                    existing_feat_count += 1

        return {
            "num_samples_with_video_path": video_path_count,
            "num_samples_with_resolved_feat_path": feat_path_count,
            "num_samples_with_existing_feat_path": existing_feat_count,
        }

    def summary(self) -> Dict[str, Any]:
        ann_count = self.count_annotations()
        path_count = self.count_paths()

        return {
            "dataset": self.dataset_name,
            "task": self.task_name,
            "seed": self.seed,
            "protocol_version": self.protocol_version,
            "mode": self.mode,
            "num_samples": len(self.samples),
            **path_count,
            "load_features": self.load_features,
            "feature_dir": self.feature_dir,
            "feature_ext": self.feature_ext,
            "feature_norm": self.feature_norm,
            "num_annotations": ann_count["total"],
            "num_old_annotations": ann_count["old"],
            "num_new_annotations": ann_count["new"],
            "num_unknown_type_annotations": ann_count["unknown_type"],
            "num_old_classes": len(self.old_classes),
            "num_new_classes": len(self.new_classes),
        }

    def print_summary(self, show_classes: bool = True, show_counts: bool = True) -> None:
        print("=" * 80)
        print("THUMOS14-TAD-GCD Dataset")
        print("=" * 80)

        for k, v in self.summary().items():
            print(f"{k}: {v}")

        if show_classes:
            print("-" * 80)
            print("Old classes:")
            for i, c in enumerate(self.old_classes):
                print(f"  {i:02d}: {c}")

            print("New classes:")
            for i, c in enumerate(self.new_classes):
                print(f"  {i:02d}: {c}")

        if show_counts:
            print("-" * 80)
            print("Annotation count by class:")
            counts = self.count_by_class()
            if counts:
                for k, v in counts.items():
                    print(f"  {k}: {v}")
            else:
                print("  no annotations")

        print("=" * 80)


